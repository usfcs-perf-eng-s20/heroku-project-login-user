package com.example.loginuser.model;

public class User {

    String userName;
    String email;
    int age;
    String city;
    String password;

    public String getUserName() {
        return userName;
    }

    public String getEmail() {
        return email;
    }

    public int getAge() {
        return age;
    }

    public String getCity() {
        return city;
    }

    public String getPassword() {
        return password;
    }


}


